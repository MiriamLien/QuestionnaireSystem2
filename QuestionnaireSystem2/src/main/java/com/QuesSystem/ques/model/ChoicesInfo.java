package com.QuesSystem.ques.model;

public class ChoicesInfo {

	private String choices;
	
	private int count;
	

	public String getChoices() {
		return choices;
	}

	public void setChoices(String choices) {
		this.choices = choices;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}
}
